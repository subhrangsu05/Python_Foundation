tuple1 = (10,20,'Mango','Apple',52.48,10,73,9873,10)

# If we want to count a particular value in the tuple:-
print(tuple1.count(10))

# if we want the index number for a particular values in the tuple:-
print(tuple1.index(10)) #--> If tuples contains a particular value more than one times the index() will show the first index occurence